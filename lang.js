// ============================================================
// LANGUE ACTIVE — détection + application
// ============================================================

function detectLang() {
  // 1. Préférence sauvegardée
  const saved = localStorage.getItem('packoo_lang');
  if (saved === 'fr' || saved === 'en') return saved;
  // 2. Langue du navigateur/Telegram
  const nav = (navigator.language || navigator.userLanguage || 'fr').toLowerCase();
  return nav.startsWith('fr') ? 'fr' : 'en';
}

function t(key) {
  const lang = window.PACKOO_LANG || 'fr';
  return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || TRANSLATIONS['fr'][key] || key;
}

function setLang(lang) {
  window.PACKOO_LANG = lang;
  localStorage.setItem('packoo_lang', lang);
  applyLang();
}

function applyLang() {
  // Éléments avec data-t="key"
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.getAttribute('data-t');
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });
  // Éléments avec data-t-html="key" (contenu HTML)
  document.querySelectorAll('[data-t-html]').forEach(el => {
    el.innerHTML = t(el.getAttribute('data-t-html'));
  });
}

// Init au chargement
window.PACKOO_LANG = detectLang();
