function collectQuest(key) {
  let q;
  if (key.startsWith('w_')) q = state.questsWeekly[key.slice(2)];
  else if (key.startsWith('d_')) q = state.defis[key.slice(2)];
  else q = state.questsDaily[key];
  if (!q) return;
  // Vérifier pas encore réclamée
  if (q.claimed) return;
  // Vérifier complète
  let isDone = false;
  if (key.startsWith('d_')) {
    // Pour les défis, vérifier aussi la condition en temps réel
    const unlockedCount = ALL_DOGS.filter(dog => dog.unlocked).length;
    if (key === 'd_reach10')  isDone = state.playerLevel >= 10;
    else if (key === 'd_unlock5')   isDone = unlockedCount >= 5;
    else if (key === 'd_earn100k')  isDone = (state.totalBonesEarned||0) >= 100000;
    else isDone = q.done === true;
    if (isDone) q.done = true; // synchroniser l état
  } else if (key === 'login') {
    isDone = true;
  } else {
    isDone = q.done || (q.progress !== undefined && q.progress >= (key === 'tap50' ? 50 : key === 'tap200' ? 200 : key === 'unlock' ? 1 : key === 'w_tap1000' ? 1000 : key === 'w_tap5000' ? 5000 : key === 'w_unlock3' ? 3 : 1));
  }
  if (!isDone) { showToast(t('toast_challenge_incomplete')); return; }
  q.claimed = true;
  q.done    = true;
  const r = QUEST_REWARDS[key];
  if (!r) return;
  const bonesVal = typeof r.bones === 'string' && r.bones.startsWith('prod_')
    ? getBonesReward(parseFloat(r.bones.replace('prod_','').replace('h','')))
    : (r.bones || 0);
  state.bones    += bonesVal;
  state.diamonds += r.diamonds;
  const msg = [];
  if (bonesVal)   msg.push('🦴 +' + fmt(bonesVal));
  if (r.diamonds) msg.push('💎 +' + r.diamonds);
  showToast(msg.join(' ') + ' réclamés !');
  updateUI(); renderQuests(); saveState();
}

