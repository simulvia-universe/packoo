  const xpTotal      = state.passXP || 0;
  const level        = Math.min(MAX_LEVEL, Math.floor(xpTotal / XP_PER_LEVEL) + 1);
  const xpInLevel    = xpTotal % XP_PER_LEVEL;
  const pct          = Math.round(xpInLevel / XP_PER_LEVEL * 100);

